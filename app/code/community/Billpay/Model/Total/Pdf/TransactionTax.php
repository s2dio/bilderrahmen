class Billpay_Model_Total_Pdf_TransactionTax extends Mage_Sales_Model_Order_Pdf_Total_Default {
	
    public function getTotalsForDisplay() {
        $amount = $this->getOrder()->formatPriceTxt($this->getAmount());
        if ($this->getAmountPrefix()) {
            $amount = $this->getAmountPrefix().$amount;
        }
        $label = Mage::helper('sales')->__($this->getTitle()) . ':';
        $fontSize = $this->getFontSize() ? $this->getFontSize() : 7;
        $total = array(
            'amount'    => $amount . ')',
            'label'     => '(' . $label,
            'font_size' => 6
        );
        return array($total);
    }

    /**
     * Check if we can display total information in PDF
     *
     * @return bool
     */
    public function canDisplay() {
        $amount = $this->getAmount();
        return $this->getDisplayZero() || ($amount != 0);
    }

    /**
     * Get Total amount from source
     *
     * @return float
     */
    public function getAmount() {
    	return $this->getSource()->getBillpayRateFeeTax();
    }
}
